#!/bin/bash
set -e
cd /home/user/Hih
mkdir -p audio

# Scene narration (Thai) — timed so each fits ~9s at 140 wpm
declare -a LINES=(
  "สวัสดีครับ วันนี้จะมาสอน วิธีทำคลิปโซเชียลแนวตั้ง ด้วย คล็อด ดีไซน์ แบบง่าย สวย เสร็จไว"
  "ขั้นตอนที่หนึ่ง บอก คล็อด ว่าอยากได้คลิปแบบไหน พิมพ์ไอเดีย ธีม และความยาว เช่น คลิปเก้าต่อสิบหก สอนทำอาหาร หนึ่งนาที"
  "ขั้นตอนที่สอง เลือกอัตราส่วน เก้าต่อสิบหก เพื่อให้คลิปพอดีกับ ติ๊กต๊อก รีล และ ชอร์ต"
  "ขั้นตอนที่สาม ใส่เนื้อหา ข้อความบรรยาย เลือกฟอนต์ สี และภาพประกอบ ให้ตรงกับสไตล์ของคุณ"
  "ขั้นตอนที่สี่ กดส่งออกเป็นไฟล์ เอ็ม พี สี่ ขนาด หนึ่งพันแปดสิบคูณหนึ่งพันเก้าร้อยยี่สิบ แล้วนำไปโพสต์ได้ทุกแพลตฟอร์ม"
  "ถ้าชอบคลิปนี้ อย่าลืมกดไลก์ และกดติดตาม เพื่อดูเทคนิคเพิ่มเติม เจอกันใหม่คลิปหน้าครับ"
)

for i in "${!LINES[@]}"; do
  idx=$((i+1))
  espeak-ng -v th -s 145 -p 45 -a 180 -g 3 "${LINES[$i]}" -w "audio/line${idx}.wav"
  echo "line${idx}: $(ffprobe -v error -show_entries format=duration -of csv=p=0 audio/line${idx}.wav)s"
done
